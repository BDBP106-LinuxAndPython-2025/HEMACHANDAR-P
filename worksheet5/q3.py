# (3) How are packages different from modules? How are modules different from functions?
#
# Modules: A module is a file containing Python definitions and statements.
# For example, a file called mymodule.py is a module. You can use modules to group related functions together
# and import them into other scripts.
#
# Packages: A package is a collection of Python modules in a directory.
# A package must contain an __init__.py file to be recognized as a package.
#
# Functions: Functions are reusable pieces of code that perform a specific task.
# A function is defined using the def keyword, and can be called multiple times in the program.